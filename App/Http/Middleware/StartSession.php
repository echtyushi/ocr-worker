<?php

namespace App\Http\Middleware;

use Closure;
use Framework\Http\Request;

class StartSession
{
    /**
     * Handle an incoming request.
     *
     * @param Request $request
     * @param Closure $next
     * @return mixed
     */
    public function handle(Request $request, Closure $next)
    {
        return $next($request);
    }
}